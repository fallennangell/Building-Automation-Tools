email_config = {
    'sender_email': 'your_email@gmail.com',
    'sender_password': 'your_email_password',
    'receiver_email': 'receiver_email@gmail.com',
    'smtp_server': 'smtp.gmail.com',
    'smtp_port': 587
}

# Dictionary to store IP-MAC mappings for ARP spoofing detection
ip_mac_mapping = {}

# Dictionary to store packet counts per source IP
packet_counts = defaultdict(int)

# Threshold for packet rate (change as needed)
threshold = 1000  # Example threshold of 1000 packets per second

def send_email_alert(subject, message):
    # Set up SMTP connection
    server = smtplib.SMTP(email_config['smtp_server'], email_config['smtp_port'])
    server.starttls()
    server.login(email_config['sender_email'], email_config['sender_password'])

    # Construct email message
    msg = MIMEMultipart()
    msg['From'] = email_config['sender_email']
    msg['To'] = email_config['receiver_email']
    msg['Subject'] = subject
    msg.attach(MIMEText(message, 'plain'))

    # Send email
    server.sendmail(email_config['sender_email'], email_config['receiver_email'], msg.as_string())

    # Close connection
    server.quit()

def print_and_send_alert(message):
    print(message)
    send_email_alert("Security Alert", message)
    print("Email Send Successfully")

def ping_scan_callback(pkt):
    if ICMP in pkt and pkt[ICMP].type == 8:  # Check if packet is ICMP Echo Request
        print_and_send_alert(f"Ping scan detected from: {pkt.src}")

def ssh_detection_callback(pkt):
    if TCP in pkt and pkt[TCP].dport == 22:  # Check if packet is TCP and destination port is 22 (SSH)
        print_and_send_alert(f"SSH traffic detected from: {pkt.src}")

def ftp_detection_callback(pkt):
    if TCP in pkt and (pkt[TCP].dport == 21 or pkt[TCP].sport == 21):  # Check if packet is TCP and destination or source port is 21 (FTP)
        print_and_send_alert(f"FTP traffic detected from: {pkt.src}")

def http_detection_callback(pkt):
    if TCP in pkt and (pkt[TCP].dport == 80 or pkt[TCP].sport == 80):  # Check if packet is TCP and destination or source port is 80 (HTTP)
        print_and_send_alert(f"HTTP traffic detected from: {pkt.src}")

def dns_detection_callback(pkt):
    if DNS in pkt:  # Check if packet is DNS
        print_and_send_alert(f"DNS traffic detected from: {pkt.src}")

def snmp_detection_callback(pkt):
    if SNMP in pkt:  # Check if packet is SNMP
        print_and_send_alert(f"SNMP traffic detected from: {pkt.src}")

def arp_spoofing_callback(pkt):
    if ARP in pkt and pkt.op == 2:  # Check if packet is ARP reply
        ip = pkt.psrc
        mac = pkt.hwsrc
        if ip in ip_mac_mapping:
            if ip_mac_mapping[ip] != mac:
                print_and_send_alert(f"ARP Spoofing detected! IP: {ip}, MAC: {mac}, Previous MAC: {ip_mac_mapping[ip]}")
        else:
            ip_mac_mapping[ip] = mac

def dos_ddos_detection_callback(pkt):
    if IP in pkt:  # Check if packet is IP
        src_ip = pkt[IP].src
        packet_counts[src_ip] += 1

        # Check packet rate for each source IP
        if packet_counts[src_ip] > threshold:
            print_and_send_alert(f"Potential DoS/DDoS attack detected from {src_ip}!")

def start_packet_detection():
    print("Starting packet detection...")
    while True:
        packet_counts.clear()  # Clear packet counts at the start of each monitoring interval
        sniff(prn=ping_scan_callback, filter="icmp", store=0, quiet=False)
        sniff(prn=ssh_detection_callback, filter="tcp", store=0, quiet=False)
        sniff(prn=ftp_detection_callback, filter="tcp", store=0, quiet=False)
        sniff(prn=http_detection_callback, filter="tcp", store=0, quiet=False)
        sniff(prn=dns_detection_callback, filter="udp and port 53", store=0, quiet=False)
        sniff(prn=snmp_detection_callback, filter="udp and port 161", store=0, quiet=False)
        sniff(prn=arp_spoofing_callback, filter="arp", store=0, quiet=False)
        sniff(prn=dos_ddos_detection_callback, store=0, timeout=1)  # Monitor traffic for 1 second intervals

if __name__ == "__main__":
    start_packet_detection()